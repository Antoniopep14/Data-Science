La sintaxis para utilizar una variable en este lenguaje de programación es:
años <- 18 

En el lenguaje de programación R los tipos básicos de datos son:
Numeric números reales: 1.
- Complex números complejos, por ejemplo: 56+4i, donde
 “i” es la unidad imaginaria.
- Character cadenas alfanuméricas delimitadas por
 comillas: hola.
- Logical valores lógicos de verdadero (TRUE) o falso
 (FALSE): x ! = false.

-Operadores lógicos:permiten determinar si la comparación entre dos o
más variables es verdadera (TRUE) o falsa (FALSE).
    && And (Y) D<-12 ; E<-6 ; F<-2 
    (D>E) && (E>F)
    FALSE

    || Or (O) D<-12 ; E<-6 ; F<-2
    (D>E) || (E>F)
    TRUE

    ! No(!) D<-12
    D!=12
    FALSE

-Operadores relacionales:son útiles para comparar el valor de dos o más variables.}
    >   Mayor que   num1>5
    <   Menor que   Num2<78
    >=  Mayor o igual que   edad>=18
    <=  Menor o igual que   año_nacimiento<=1984
    ==  Igual que   monto==2584
    ¡=  Distinto que    valor!=2
    
-Operadores aritméticos: se utilizan para realizar operaciones numéricas con
    los contenidos de las variables o asignar el resultado de cierta
    operación a una variable.

    + Suma      a<-184+10
    - Resta     b<-18-6
    * Multiplicación c<- a*b
    / División  d<-x/6
    ^ Potencia  e<- 3 ^ 3
